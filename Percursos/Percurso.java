package Percursos;

public class Percurso {
    private int codigo;
    private String nome;
    private double distancia;
    private String cidade;

    public Percurso(String nome, double distancia, String cidade) {
        this.codigo = 0;
        this.nome = nome;
        this.distancia = distancia;
        this.cidade = cidade;
    }
}
